public class chanLeTu1Den10 {
    public static void main(String[] args) {
        for(int i = 0; i <= 10; i += 2) {//Khởi tạo vòng lặp for in ra các số chẵn từ 0 đến 10 sau mỗi vòng + thêm 2 để in ra các số chẵn từ 0 đến 10
            System.out.println();
        }
        for(int i = 1; i <= 10; i += 2) {//Khởi tạo vòng lặp for in ra các số lẻ từ 0 đến 10 sau mỗi vòng + thêm 2 để in ra các số lẻ từ 0 đến 10
            System.out.println();
        }
    }
}
