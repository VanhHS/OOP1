public class hienthikytu {
    public static void main(String[] args) {
        String str = "hế lô hao a diu";//nhập chuỗi
        for (int i = 0;i<=str.length();i++){//dùng vòng lặp for để duyệt chuỗi
            System.out.println(str.charAt(i));//sử dụng hàm charAt để in ra từng kí tự trong chuỗi
        }
    }
}
